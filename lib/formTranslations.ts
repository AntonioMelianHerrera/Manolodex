// Función para obtener el nombre formateado de una forma
export function getFormattedFormName(pokemonName: string, formName: string): string {
  const fullName = `${pokemonName}-${formName}`;
  
  // Buscar en el diccionario de traducciones
  if (FORM_TRANSLATIONS[formName]) {
    return FORM_TRANSLATIONS[formName];
  }
  if (FORM_TRANSLATIONS[fullName]) {
    return FORM_TRANSLATIONS[fullName];
  }
  
  // Si no está en el diccionario, hacer transformación automática
  // Convertir guiones a espacios y capitalizar
  const formatted = formName
    .split('-')
    .map(word => word.charAt(0).toUpperCase() + word.slice(1))
    .join(' ');
  
  return formatted;
}
